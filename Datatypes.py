# Python program to demonstrate numeric data types

# Integer: Represents whole numbers
a = 5
print("The data type of variable 'a' is:\t", type(a))

# Floating-point: Represents decimal numbers
b = 5.2
print("The data type of variable 'b' is:\t", type(b))

# Complex numbers: Represents numbers with real and imaginary parts
z = 2 + 3j
print("The data type of variable 'z' is:\t", type(z))
